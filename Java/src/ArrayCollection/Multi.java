package ArrayCollection;

public class Multi
{
	public static void main(String[] args)
	{
		int [][]x=new int[2][2];
		x[0][0]=10;
		x[0][1]=20;
		x[1][0]=30;
		x[1][1]=40;
		for(int i=0;i<2;i++)
		{
		for(int j=0;j<2;j++)
		{
			System.out.print(x[i][j]+"\t");
			
		}
	}
	}

}
